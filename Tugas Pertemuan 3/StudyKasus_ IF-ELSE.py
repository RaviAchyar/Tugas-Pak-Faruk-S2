# IF ELSE

# Input bilangan
bilangan = int(input("Masukkan sebuah bilangan: "))

# Menentukan apakah bilangan ganjil atau genap
if bilangan % 2 == 0:
    print(f"{bilangan} adalah bilangan genap.")
else:
    print(f"{bilangan} adalah bilangan ganjil.")
    
